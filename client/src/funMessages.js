let lastIndex = {};

export function getRandomMessage(category) {
  const list = messages[category] || messages.downloading;
  let idx = lastIndex[category];
  if (idx === undefined || idx >= list.length - 1) {
    idx = -1;
  }
  idx++;
  lastIndex[category] = idx;
  return list[idx];
}

export function getMessages(category) {
  return messages[category] || [];
}
